    private void labelAddPhotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelAddPhotoMouseClicked
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.setFileFilter(new FileFilter() { // untuk memfiler type png dan jpg
                @Override
                public boolean accept(File file) {
                    String n = file.getName().toLowerCase();
                    if(file.isDirectory() || n.endsWith(".jpg") || n.endsWith(".png")){
                        return true;
                    }else{
                        return false;
                    }
                }

                @Override
                public String getDescription() {
                    return ".jpg | .png";
                }
            });
            int click = chooser.showOpenDialog(this);
            if (click == JFileChooser.APPROVE_OPTION){

                filename = chooser.getSelectedFile().getAbsolutePath();
                System.out.println(filename);
                labelAddPhoto.setText("");
                labelAddPhoto.setIcon(resizeImage(new ImageIcon(filename)));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_labelAddPhotoMouseClicked
    private ImageIcon resizeImage(ImageIcon img){
        return new ImageIcon(img.getImage().getScaledInstance(labelAddPhoto.getWidth()-2, labelAddPhoto.getHeight()-2,Image.SCALE_SMOOTH));
    }
