    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

            String newpath = "src////seller";
            File directory = new File(newpath);
            if (!directory.exists()){
                directory.mkdir();
            }
            File fileAwal = null;
            File fileAkhir = null;
            String ext = this.filename.substring(filename.lastIndexOf('.')+1);
            fileAwal = new File(filename);
            fileAkhir = new File(newpath+"////"+txtEmail.getText()+"."+ext);

            st = cn.createStatement();
            if (txtEmail.getText().equals("") || txtNo.getText().equals("") || txtPassword.getText().equals("") || txtRePassword.getText().equals("") || txtNamaToko.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong","Validasi Data",JOptionPane.ERROR_MESSAGE);
                return;
            }else{
                String cek="SELECT * FROM seller WHERE email = '" + txtEmail.getText()+"'";
                rs = st.executeQuery(cek);

                if (rs.next()){ //mengecek apabila emailnya sama
                    JOptionPane.showMessageDialog(null, "Maaf Email sudah digunakan");
                }else{
                    if (txtPassword.getText().equals(txtRePassword.getText())){
                        
                        String sql = "INSERT INTO seller VALUES ('"+txtEmail.getText()+
                        "','"+txtNo.getText()+
                        "','"+txtPassword.getText()+
                        "','"+0+
                        "','"+fileAkhir+
                        "','"+txtNamaToko.getText()+"')";
                        st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                        Files.copy(fileAwal.toPath(), fileAkhir.toPath());

                        Bersih();

                        this.dispose();
                        LogInSeller lgS = new LogInSeller();
                        lgS.setVisible(true);
                        //                    lg.setLocationRelativeTo(null);
                    }else{
                        labelWarning.setText("Password tidak sama");
                        return;
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

